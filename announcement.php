            let lastViewed = null;
        
            function showBlog(id) {
                // Hide all blog sections
                document.querySelectorAll('.blog-content').forEach(blog => {
                    blog.style.display = 'none';
                });
        
                // Show the selected blog section
                const blogElement = document.getElementById('blog-' + id);
                if (blogElement) blogElement.style.display = 'block';
        
                // Hide "Other Announcements" section
                const otherAnnouncements = document.querySelector('.other-announcements');
                if (otherAnnouncements) otherAnnouncements.style.display = 'none';
        
                // Show the blog section
                const blogSection = document.getElementById('blog-section');
                if (blogSection) blogSection.style.display = 'block';
        
                // Restore the last hidden announcement in "Other Announcements"
                if (lastViewed !== null) {
                    const lastOtherAnnouncement = document.getElementById('other-announcement-' + lastViewed);
                    if (lastOtherAnnouncement) lastOtherAnnouncement.style.display = 'block';
                }
        
                // Hide the clicked announcement in both sections
                const clickedAnnouncement = document.getElementById('announcement-' + id);
                const clickedOtherAnnouncement = document.getElementById('other-announcement-' + id);
        
                if (clickedAnnouncement) clickedAnnouncement.style.display = 'none';
                if (clickedOtherAnnouncement) clickedOtherAnnouncement.style.display = 'none';
        
                // Store the currently hidden announcement
                lastViewed = id;
        
                // Scroll to the top smoothly
                window.scrollTo({ top: 0, behavior: 'smooth' });
            }
        
            function showAnnouncements() {
                // Hide the blog section
                const blogSection = document.getElementById('blog-section');
                if (blogSection) blogSection.style.display = 'none';
        
                // Show "Other Announcements" section
                const otherAnnouncements = document.querySelector('.other-announcements');
                if (otherAnnouncements) otherAnnouncements.style.display = 'block';
        
                // Restore the last hidden announcement in "Other Announcements"
                if (lastViewed !== null) {
                    const lastOtherAnnouncement = document.getElementById('other-announcement-' + lastViewed);
                    if (lastOtherAnnouncement) lastOtherAnnouncement.style.display = 'block';
        
                    // Clear lastViewed since we're returning to the announcements view
                    lastViewed = null;
                }
        
                // Scroll to the top smoothly when returning to announcements
                window.scrollTo({ top: 0, behavior: 'smooth' });
            }
